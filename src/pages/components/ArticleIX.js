export default function ArticleIX() {
    return(
        <article>

                <section>
                <h2>Article IX: Party walls</h2>
                    <p><strong>SECTION 1: <u>General Rules of Law Apply</u></strong> 
                    . Each wall which is built is a part of the original construction of the homes upon the properties and placed on the dividing line between the lot shall constitute a party wall, 
                    and, to the extent not inconsistent with the provisions of this Article, the general rule of law regarding party walls and liability for property damage due to negligent or 
                    willful acts or emissions shall apply thereto.
                    </p>  
                </section>

                <section>
                    <p><strong>SECTION 2: <u>Sharing of Repair and Maintenance</u></strong> 
                    . The cost of reasonable repair and maintenance of a party wall shall be shared by the owners who make use of the wall in proportion to such use.
                    </p>  
                </section>

                <section>
                    <p><strong>SECTION 3: <u>Destruction by Fire or Other Casualty</u></strong> 
                    . If a party wall is destroyed or damaged by fire or other casualty, any owner who has used the wall may restore it, and if the other owners thereafter make use of the wall, 
                    they shall contribute to the cost of restoration thereof in proportion to such use without prejudice, however, to the right of any such owners to call for a larger contribution 
                    from the others under any rule of law regarding liability from negligent or willful acts or omissions.
                    </p>  
                </section>

                <section>
                    <p><strong>SECTION 4: <u>Weatherproofing</u></strong> 
                    . Notwithstanding any other provision of this Article, an owner who by his negligence or willful act causes the party wall to be exposed to the elements shall bear the whole cost 
                    of furnishing the necessary protection against such elements.
                    </p>  
                </section>

                <section>
                    <p><strong>SECTION 5: <u>Right to Contribution Runs with Land</u></strong> 
                    . The right of any owner to contribution from any other owner under this Article shall be appurtenant to the land and shall pass to such owner's successors and title.
                    </p>  
                </section>

                <section>
                    <p><strong>SECTION 6: <u>Arbitration</u></strong> 
                    . In the event of any dispute arising concerning a party wall, or under the provisions of this Article, each party shall choose an arbitrator, and such arbitrator shall choose one 
                    additional arbitrator, and the decision shall be by a majority of all the arbitrators.
                    </p>  
                </section>

            </article>
    )
}